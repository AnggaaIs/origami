import { COLORS } from "@origami/classes/Constants";

export function toTitleCase(str: string): string {
  return str.replace(
    /\w\S*/g,
    (txt) => txt.charAt(0).toUpperCase() + txt.substr(1).toLowerCase(),
  );
}

export function convertToHex(color: string): number {
  if (!COLORS[color]) throw new Error("Invalid color");
  return parseInt(COLORS[color].replace(/#/g, "0x"));
}
